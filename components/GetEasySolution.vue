  }
  if (email && email.trim()) {
    contactInfo.push(`Email: ${email}`);
  }
  const contactString = contactInfo.join('\n');
  
  let subject = '';
  let content = '';
  
  if (props.getEasySolutionType === 2) {
    subject = `Support Request from ${name}`;
    content = `
