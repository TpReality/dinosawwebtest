const validateForm = () => {
  const { types, materials, name, phone, email } = form.value;
  
  // å¯¹äºäº§å“è¯¢ä»·è¡¨å•ï¼ˆç±»å‹1å’Œ3ï¼‰ï¼Œéœ€è¦éªŒè¯ types å’Œ materials
  if (props.getEasySolutionType === 1 || props.getEasySolutionType === 3) {
    if (!types || types.trim() === '') {
      showMessage('warning', 'Please select a CNC machine type.');
      return false;
    }
    
    if (!materials || materials.trim() === '') {
      showMessage('warning', 'Please select the materials you will be working with.');
      return false;
    }
  }
  
  if (!name.trim()) {
    showMessage('warning', 'Please enter your name.');
    return false;
  }
  
  // éªŒè¯æ‰‹æœºå·å’Œé‚®ç®±ï¼Œè‡³å°‘å¡«å†™ä¸€ä¸ª
  const hasPhone = phone && phone.trim() !== '';
  const hasEmail = email && email.trim() !== '';
  
  if (!hasPhone && !hasEmail) {
    showMessage('warning', 'è¯·è‡³å°‘å¡«å†™æ‰‹æœºå·æˆ–é‚®ç®±å…¶ä¸­ä¸€é¡¹');
    return false;
  }
  
  // å¦‚æœå¡«å†™äº†æ‰‹æœºå·ï¼ŒéªŒè¯æ ¼å¼
  if (hasPhone) {
    const phoneRegex = /^[1-9]\d{6,14}$/; // ç®€åŒ–çš„æ‰‹æœºå·éªŒè¯
    if (!phoneRegex.test(phone.replace(/[\s\-]/g, ''))) {
      showMessage('warning', 'è¯·è¾“å…¥æœ‰æ•ˆçš„æ‰‹æœºå·');
      return false;
    }
  }
  
  // å¦‚æœå¡«å†™äº†é‚®ç®±ï¼ŒéªŒè¯æ ¼å¼
  if (hasEmail) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      showMessage('warning', 'è¯·è¾“å…¥æœ‰æ•ˆçš„é‚®ç®±åœ°å€');
      return false;
    }
  }
  
  return true;
}

const buildEmailContent = () => {
  const { types, materials, name, countryCode, phone, email, country, yourRequirements } = form.value;
  
